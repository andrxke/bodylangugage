"""
PyTorch Dataset for ST-GCN gesture classification.

Loads labeled skeleton clips from a CSV manifest and corresponding
.npz landmark files. Handles adaptive temporal sampling (so clips
of any length fit the fixed-size ST-GCN input), hip-centering
normalization, and data augmentation for small datasets.

Data directory structure:
    data/
    ├── clips/
    │   ├── clip_001.mp4
    │   ├── clip_001.npz    ← generated by prepare_data.py
    │   ├── clip_002.mp4
    │   ├── clip_002.npz
    │   └── ...
    └── labels.csv

labels.csv format:
    filename,facing,arms_crossed,arms_hidden
    clip_001.mp4,1,0,0
    clip_002.mp4,1,1,0
    clip_003.mp4,0,0,1
"""

from __future__ import annotations

import csv
import logging
from pathlib import Path

import numpy as np
import torch
from torch.utils.data import Dataset

from classification.gesture_labels import GESTURE_TYPES

logger = logging.getLogger(__name__)


class GestureDataset(Dataset):
    """PyTorch Dataset for loading labeled skeleton clips.

    Each sample is a skeleton sequence tensor of shape (C, T, V, M),
    where C=3 (x,y,z), T=clip_length, V=33, M=1.

    Args:
        data_dir:     Root data directory (containing clips/ and labels.csv).
        gesture_type: Which gesture column to use as the label
                      (e.g., "facing", "arms_crossed", "arms_hidden").
        clip_length:  Fixed temporal dimension (default 128).
        augment:      Enable data augmentation for training.
    """

    def __init__(
        self,
        data_dir: str,
        gesture_type: str,
        clip_length: int = 128,
        augment: bool = False,
    ) -> None:
        self.data_dir = Path(data_dir)
        self.gesture_type = gesture_type
        self.clip_length = clip_length
        self.augment = augment

        if gesture_type not in GESTURE_TYPES:
            raise ValueError(
                f"Unknown gesture type '{gesture_type}'. "
                f"Valid types: {list(GESTURE_TYPES.keys())}"
            )

        self.csv_column = GESTURE_TYPES[gesture_type]["csv_column"]
        self.clips_dir = self.data_dir / "clips"

        # Load the manifest and filter to clips with .npz files.
        self.samples: list[dict] = self._load_manifest()

        logger.info(
            "GestureDataset: %d samples for '%s' (augment=%s)",
            len(self.samples),
            gesture_type,
            augment,
        )

    def _load_manifest(self) -> list[dict]:
        """Load and validate the labels.csv manifest.

        Returns:
            List of dicts with keys: 'npz_path', 'label', 'filename'.

        Raises:
            FileNotFoundError: If labels.csv is missing.
            ValueError: If the gesture column is missing from the CSV.
        """
        csv_path = self.data_dir / "labels.csv"
        if not csv_path.exists():
            raise FileNotFoundError(
                f"Labels manifest not found at {csv_path}. "
                f"Create a labels.csv with columns: "
                f"filename,{','.join(gt['csv_column'] for gt in GESTURE_TYPES.values())}"
            )

        samples = []

        with open(csv_path, newline="") as f:
            reader = csv.DictReader(f)

            if self.csv_column not in reader.fieldnames:
                raise ValueError(
                    f"Column '{self.csv_column}' not found in {csv_path}. "
                    f"Available columns: {reader.fieldnames}"
                )

            for row in reader:
                filename = row["filename"].strip()

                # Find the corresponding .npz file.
                npz_name = Path(filename).with_suffix(".npz").name
                npz_path = self.clips_dir / npz_name

                if not npz_path.exists():
                    logger.warning(
                        "Skipping %s: no .npz file found at %s. "
                        "Run 'python -m training.prepare_data' first.",
                        filename,
                        npz_path,
                    )
                    continue

                label_str = row[self.csv_column].strip()
                if label_str not in ("0", "1"):
                    logger.warning(
                        "Skipping %s: invalid label '%s' for '%s' "
                        "(expected 0 or 1).",
                        filename,
                        label_str,
                        self.csv_column,
                    )
                    continue

                samples.append({
                    "npz_path": str(npz_path),
                    "label": int(label_str),
                    "filename": filename,
                })

        # Report class balance.
        positives = sum(s["label"] for s in samples)
        negatives = len(samples) - positives
        logger.info(
            "Class balance for '%s': %d positive, %d negative",
            self.gesture_type,
            positives,
            negatives,
        )

        if len(samples) == 0:
            logger.error(
                "No valid samples found. Check that labels.csv exists, "
                "has the '%s' column, and .npz files exist in %s.",
                self.csv_column,
                self.clips_dir,
            )

        return samples

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> tuple[torch.Tensor, int]:
        """Load and preprocess a single clip.

        Returns:
            Tuple of (x, label):
              - x:     Tensor of shape (C, T, V, M) = (3, clip_length, 33, 1)
              - label: Integer class label (0 or 1)
        """
        sample = self.samples[idx]

        # Load world landmarks from .npz.
        data = np.load(sample["npz_path"], allow_pickle=True)
        world_landmarks = data["world_landmarks"]  # (T_original, 33, 3)
        label = sample["label"]

        # Handle NaN frames (no detection): replace with zeros.
        nan_mask = np.any(np.isnan(world_landmarks), axis=(1, 2))
        world_landmarks[nan_mask] = 0.0

        # Adaptive temporal sampling to fit clip_length.
        world_landmarks = self._temporal_sample(world_landmarks)

        # Normalize: subtract hip midpoint per frame.
        world_landmarks = self._normalize(world_landmarks)

        # Data augmentation (training only).
        if self.augment:
            world_landmarks = self._augment(world_landmarks)

        # Reshape to (C, T, V, M).
        # world_landmarks is (T, V, C) → (C, T, V) → add M dim
        x = world_landmarks.transpose(2, 0, 1)  # (C, T, V)
        x = x[:, :, :, np.newaxis]               # (C, T, V, 1)

        return torch.tensor(x, dtype=torch.float32), label

    def _temporal_sample(self, wl: np.ndarray) -> np.ndarray:
        """Adaptively sample/pad to fit the target clip_length.

        - Short clips: zero-pad at the end.
        - Long clips: uniformly subsample to fit.

        Args:
            wl: World landmarks, shape (T_original, 33, 3).

        Returns:
            Resampled array of shape (clip_length, 33, 3).
        """
        T_original = wl.shape[0]
        T_target = self.clip_length

        if T_original == T_target:
            return wl

        if T_original < T_target:
            # Zero-pad at the end.
            pad = np.zeros(
                (T_target - T_original, wl.shape[1], wl.shape[2]),
                dtype=wl.dtype,
            )
            return np.concatenate([wl, pad], axis=0)

        # Uniformly subsample: pick T_target evenly spaced frames.
        indices = np.linspace(0, T_original - 1, T_target, dtype=int)
        return wl[indices]

    @staticmethod
    def _normalize(wl: np.ndarray) -> np.ndarray:
        """Center each frame on the hip midpoint.

        Subtracts the midpoint of left_hip (23) and right_hip (24)
        from all joints per frame. This makes the features position-
        invariant — only the relative joint positions matter.

        Args:
            wl: World landmarks, shape (T, 33, 3).

        Returns:
            Hip-centered landmarks, same shape.
        """
        hip_midpoint = (wl[:, 23, :] + wl[:, 24, :]) / 2.0  # (T, 3)
        return wl - hip_midpoint[:, np.newaxis, :]

    def _augment(self, wl: np.ndarray) -> np.ndarray:
        """Apply random data augmentation to a clip.

        Augmentation strategies designed for small skeleton datasets:
          1. Random noise on joint positions
          2. Random joint dropout (mask joints with zeros)
          3. Random temporal shift
          4. Random rotation around Y-axis
          5. Random scaling

        Args:
            wl: World landmarks, shape (T, 33, 3).

        Returns:
            Augmented landmarks, same shape.
        """
        wl = wl.copy()

        # 1. Gaussian noise on joint positions (50% chance).
        if np.random.random() < 0.5:
            noise = np.random.normal(0, 0.01, wl.shape).astype(wl.dtype)
            wl += noise

        # 2. Random joint dropout: zero out 1–3 joints (30% chance).
        if np.random.random() < 0.3:
            num_drop = np.random.randint(1, 4)
            drop_joints = np.random.choice(33, num_drop, replace=False)
            wl[:, drop_joints, :] = 0.0

        # 3. Random temporal shift: roll by ±10% of clip length (40% chance).
        if np.random.random() < 0.4:
            max_shift = max(1, self.clip_length // 10)
            shift = np.random.randint(-max_shift, max_shift + 1)
            wl = np.roll(wl, shift, axis=0)

        # 4. Random Y-axis rotation ±15° (50% chance).
        if np.random.random() < 0.5:
            angle = np.random.uniform(-np.pi / 12, np.pi / 12)
            cos_a, sin_a = np.cos(angle), np.sin(angle)
            rotation = np.array([
                [cos_a, 0, sin_a],
                [0, 1, 0],
                [-sin_a, 0, cos_a],
            ], dtype=wl.dtype)
            # Apply rotation to all frames: (T, 33, 3) @ (3, 3)
            wl = wl @ rotation.T

        # 5. Random uniform scaling ±10% (40% chance).
        if np.random.random() < 0.4:
            scale = np.random.uniform(0.9, 1.1)
            wl *= scale

        return wl

    def get_class_weights(self) -> torch.Tensor:
        """Compute inverse-frequency class weights for imbalanced data.

        Returns:
            Tensor of shape (2,) with weights for [negative, positive].
        """
        labels = [s["label"] for s in self.samples]
        counts = np.bincount(labels, minlength=2)
        total = len(labels)

        # Inverse frequency weighting, preventing division by zero.
        safe_counts = np.maximum(counts, 1)
        weights = total / (2.0 * safe_counts.astype(np.float32))

        return torch.tensor(weights, dtype=torch.float32)

    def get_split_indices(
        self,
        val_fraction: float = 0.2,
        seed: int = 42,
    ) -> tuple[list[int], list[int]]:
        """Split sample indices into train/val sets, stratified by label.

        Splits by clip (each clip is entirely in train or val) with
        stratification to maintain class balance.

        Args:
            val_fraction: Fraction of samples for validation.
            seed:         Random seed for reproducibility.

        Returns:
            Tuple of (train_indices, val_indices).
        """
        rng = np.random.RandomState(seed)

        # Separate indices by class.
        pos_indices = [i for i, s in enumerate(self.samples) if s["label"] == 1]
        neg_indices = [i for i, s in enumerate(self.samples) if s["label"] == 0]

        rng.shuffle(pos_indices)
        rng.shuffle(neg_indices)

        # Take val_fraction from each class.
        n_pos_val = max(1, int(len(pos_indices) * val_fraction))
        n_neg_val = max(1, int(len(neg_indices) * val_fraction))

        val_indices = pos_indices[:n_pos_val] + neg_indices[:n_neg_val]
        train_indices = pos_indices[n_pos_val:] + neg_indices[n_neg_val:]

        rng.shuffle(val_indices)
        rng.shuffle(train_indices)

        logger.info(
            "Split: %d train (%d pos, %d neg), %d val (%d pos, %d neg)",
            len(train_indices),
            len(pos_indices) - n_pos_val,
            len(neg_indices) - n_neg_val,
            len(val_indices),
            n_pos_val,
            n_neg_val,
        )

        return train_indices, val_indices
